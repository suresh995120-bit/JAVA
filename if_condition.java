public class if_condition {
    public static void main(String[] args) {
        int a = 30;
        int b = 30;
        if (a > b) {
            System.out.println("a is greater than b");
        } else if (a == b) {
            System.out.println("a is equal to b");
        } else {
            System.out.println("a is not greater than b");
        }
    }
}