public class string1 {
    public static void main(String[] args) {
        String s = "Revuri suresh";
        System.out.println(s.length());
        System.out.println(s.charAt(4));
        System.out.println(s.substring(0, 6));
        System.out.println(s.substring(7));
        System.out.println(s.contains("suresh"));
        System.out.println(s.contains("suresh1"));
        System.out.println(s.equals("Revuri suresh"));
        System.out.println(s.equals("revuri suresh"));
        System.out.println(s.endsWith("suresh"));
        System.out.println(s.toUpperCase());
        System.out.println(s.toLowerCase());
        System.out.println(s.indexOf("suresh"));
        
    }
}
